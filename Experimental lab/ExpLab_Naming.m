function str = parse_vel(handles)
vel = cellstr(handles.popupmenu3.String{handles.popupmenu3.Value});
if strcmp(vel, '15')
    str = '15';
elseif strcmp(vel, '25')
    str = '25';
else
    error('Error reading "Velocity"')
end

function str = parse_rep(handles)
rep = cellstr(handles.popupmenu4.String{handles.popupmenu4.Value});
if strcmp(rep, 'First')
    str = '1';
elseif strcmp(rep, 'Second')
    str = '2';
elseif strcmp(rep, 'Third')
    str = '3';
elseif strcmp(rep, 'Fourth')
    str = '4';
else
    error('Error reading "Repitition Number"')
end

