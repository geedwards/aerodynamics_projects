    error('X distance length is incorrect!');
end

filename = ['ExpLab', ...
            '_', airfoil, '_V', vel, '_x', x_dist, '_r0', rep, '.csv']; %'_', updown,
if ~isempty(x_dist)
    handles.edit2.String = filename;
else
    handles.edit2.String = 'Error: You did not specify the horizontal testing location';
end

function str = parse_airfoil(handles)
airfoil = cellstr(handles.popupmenu1.String{handles.popupmenu1.Value});
if strcmp(airfoil, 'Cylinder')
    str = 'Cylinder';
elseif strcmp(airfoil, 'Airfoil')
    str = 'Airfoil';
else
    error('Error reading "Testing Body"')
end

